% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
global MP MaxWeg Real_Target  Point1_X Point2_X 
if isempty(get(handles.edit1,'String'))
    msgbox('请输入横线纵坐标');
    return;
end 
if isempty(get(handles.edit20,'String'))
    msgbox('请输入Thickness of the foam');
    return;
end 
[filename,pathname,fileindex]=uigetfile('*.xls;*.xlsx','选择数据');
if isequal(filename,0)||isequal(pathname,0)||isequal(fileindex,0)
    msgbox('导入文件失败');
else
       cla(handles.axes2);
    t1=waitbar(0,'正在读入数据');
    Filename=strcat(pathname,filename);
    [Type Sheet Format]=xlsfinfo(Filename) ;
    for i=1:length(Sheet)
        MP{i}=xlsread(Filename,char(Sheet{1,i}));
        waitbar(i/(length(Sheet)));
    end
end
close(t1)
MaxWeg1=max(MP{1}(:,1));
MaxWeg2=max(MP{2}(:,1));
MaxWeg=max(MaxWeg1,MaxWeg2);

Real_Target=str2num(get(handles.edit1,'String'));
Cruve1_Start_index=find(MP{1}(:,2)>Real_Target,1);
Cruve1_End_index=find(MP{1}(:,2)>Real_Target,1,'last');
Cruve1_Start_X=MP{1}(Cruve1_Start_index,1);
Cruve1_End_X=MP{1}(Cruve1_End_index,1);
Point1_X=Cruve1_Start_X+(Cruve1_End_X-Cruve1_Start_X)/2;
Cruve2_Start_index=find(MP{2}(:,2)>Real_Target,1);
Cruve2_End_index=find(MP{2}(:,2)>Real_Target,1,'last');
Cruve2_Start_X=MP{2}(Cruve2_Start_index,1);
Cruve2_End_X=MP{2}(Cruve2_End_index,1);
Point2_X=Cruve2_Start_X+(Cruve2_End_X-Cruve2_Start_X)/2;

Target_Weg=Point2_X-Point1_X;

plot(handles.axes2,MP{1}(:,1),MP{1}(:,2),'b','linewidth',2)
hold on
plot(handles.axes2,MP{2}(:,1),MP{2}(:,2),'r','linewidth',2)
plot(handles.axes2,Point1_X,Real_Target,'bs','linewidth',1,'MarkerFaceColor','b')
plot(handles.axes2,Point2_X,Real_Target,'rs','linewidth',1,'MarkerFaceColor','r')
plot(handles.axes2,[0,MaxWeg*1.1],[Real_Target,Real_Target],'--k','linewidth',2)
plot(handles.axes2,[Point1_X,Point1_X],[0,Real_Target],'b','linewidth',1)
plot(handles.axes2,[Point2_X,Point2_X],[0,Real_Target],'r','linewidth',1)

 xlabel(handles.axes2,'Weg/位移[mm]','FontSize',10);ylabel(handles.axes2,'Kraft/力[kN]','FontSize',10);
 axis(handles.axes2,[0 MaxWeg*1.1 0 inf]);grid on;grid minor;        
 set(handles.edit2,'string',num2str(Point1_X,'%.2f'))
  set(handles.edit3,'string',num2str(Point2_X,'%.2f'))
 set(handles.edit4,'string',num2str(Target_Weg,'%.2f'))
  set(handles.edit6,'string',[get(handles.edit1,'String'),'N'])
 
  %19.8.27修改，加入表格内容，查找一些特定值填入表格
  EDIT11_INDEX=find(MP{1}(:,2)>1,1);
  EDIT11=MP{1}(EDIT11_INDEX,1);
  EDIT12=MP{1}(Cruve1_Start_index,1);  
  EDIT13_INDEX=find(MP{2}(:,2)>1,1);
  EDIT13=MP{2}(EDIT13_INDEX,1);
  EDIT14=MP{2}(Cruve2_Start_index,1); 
 
  EDIT15= EDIT13- EDIT11;
  EDIT16= EDIT14- EDIT12;
  
  plot(handles.axes2,MP{1}(EDIT11_INDEX,1),MP{1}(EDIT11_INDEX,2),'bo','linewidth',1,'MarkerFaceColor','b')
  plot(handles.axes2,MP{2}(EDIT13_INDEX,1),MP{2}(EDIT13_INDEX,2),'ro','linewidth',1,'MarkerFaceColor','r')
  set(handles.edit11,'string',[num2str(EDIT11,'%.2f'),'mm'])
  %set(handles.edit12,'string',[num2str(EDIT12,'%.2f'),'mm'])  
  set(handles.edit12,'string',[get(handles.edit2,'String'),'mm'])  
  set(handles.edit13,'string',[num2str(EDIT13,'%.2f'),'mm'])
  %set(handles.edit14,'string',[num2str(EDIT14,'%.2f'),'mm'])
   set(handles.edit14,'string',[get(handles.edit3,'String'),'mm'])  
  set(handles.edit15,'string',[num2str(EDIT15,'%.2f'),'mm'])
  %set(handles.edit16,'string',[num2str(EDIT16,'%.2f'),'mm'])
  set(handles.edit16,'string',[get(handles.edit4,'String'),'mm'])  
  set(handles.edit17,'string','N/A')
    if  EDIT12==0
    set(handles.edit18,'string','N/A')
  else 
      set(handles.edit18,'string',[num2str(100*str2num(get(handles.edit4,'String'))/str2num(get(handles.edit20,'String')),'%.2f'),'%'])
  end   
  legend({'','','',''},'Location','NorthWest')
set(handles.pushbutton2,'enable','on')




